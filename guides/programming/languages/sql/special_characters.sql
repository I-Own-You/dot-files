$digit ($1,$2, .etc) -- represent a positional parameter in the body of a function definition or a prepared statement
() -- group expressions and enforce precedence, sometimes required as part of the fixed syntax of a particular SQL command.
[] -- select the elements of an array
, -- used in some syntactical constructs to separate the elements of a list.
; -- terminate sql command, except being in a string literal
: -- select “slices” from arrays, in certain SQL dialects (such as Embedded SQL), the colon is used to prefix variable names.
* -- used in some contexts to denote all the fields of a table row or composite value.
  -- special meaning when used as the argument of an aggregate function, aggregate does not require any explicit parameter.
. -- used in numeric constants, and to separate schema, table, and column names.
